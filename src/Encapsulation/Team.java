package Encapsulation;

public class Team {
	
	private String teamName,teamCapt,teamSize,teamAchievment,teamCoach;

	public String getTeamName() {
		return teamName;
	}

	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}

	public String getTeamCapt() {
		return teamCapt;
	}

	public void setTeamCapt(String teamCapt) {
		this.teamCapt = teamCapt;
	}

	public String getTeamSize() {
		return teamSize;
	}

	public void setTeamSize(String teamSize) {
		this.teamSize = teamSize;
	}

	public String getTeamAchievment() {
		return teamAchievment;
	}

	public void setTeamAchievment(String teamAchievment) {
		this.teamAchievment = teamAchievment;
	}

	public String getTeamCoach() {
		return teamCoach;
	}

	public void setTeamCoach(String teamCoach) {
		this.teamCoach = teamCoach;
	}
	


}

